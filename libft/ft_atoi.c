#include "libft.h"

int	ft_atoi(const char *str)
{
	int				i;
	int				b;
	long int		r;

	i = 0;
	r = 0;
	b = 1;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == ' '))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			b *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = r * 10 + (str[i] - 48);
		i++;
		if (r > 2147483648 && b == -1)
			return (0);
		if (r > 2147483647 && b == 1)
			return (-1);
	}
	return (r * b);
}
